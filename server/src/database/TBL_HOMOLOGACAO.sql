  DTLANCAMENTO DATETIME DEFAULT NULL,
  DTVENCIMENTO DATETIME DEFAULT NULL,
  OBSERVACAO   VARCHAR(500),
  FORNECEDOR   INT(10),
  STATUS       ENUM('A','P','T') DEFAULT 'A',/*A->ABERTO,P->PARCIAL, T->TOTAL*/
  ORIGEM       VARCHAR(10),
  TIPODOC      INT(10),
  DTCRIADO     TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
  DTALTERACAO  TIMESTAMP on UPDATE CURRENT_TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP ,
  PRIMARY KEY(EMPRESA, ID),
  KEY (ID)
);
COMMIT;

