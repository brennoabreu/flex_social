CREATE TABLE TBLPAGAR (
  EMPRESA      VARCHAR(40) NOT NULL,
  ID           INT(10) NOT NULL AUTO_INCREMENT,
  DOCUMENTO    VARCHAR(20),
  TIPO	       ENUM('D','C'),/*D-DEBITO, C-CREDITO*/
